  public void refreshTable(){
        int rowCount = jTable1.getRowCount();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for(int i=rowCount-1;i>=0;i--){
            model.removeRow(i);
        }
        if(menu!=null){
        for(Food food:menu.getMenu()){
            Object row[] = new Object[2];
            row[0] = food;
            row[1] = food.getPrice();
            model.addRow(row);  
        }
        }
    }

